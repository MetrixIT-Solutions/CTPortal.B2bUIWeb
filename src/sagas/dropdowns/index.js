import { call, put, takeLatest } from 'redux-saga/effects';
import { postApiCall } from '../../server/ApiCallManager';
import apis from '../../../config/apis.json';

const errEmptyRes = {};
const timeOutRes = { status: '777', message: 'Server not responding' };

//--- Begin: Saga main Action Watcher generator function
function* WatchDropdownsSaga() {
  yield takeLatest('POST_B2B_DROPDOWNS_LIST', workerPostB2BDropdownsList);
  yield takeLatest('POST_B2B_DROPDOWNS_CREATE', workerPostB2BDropdownsCreate);
  yield takeLatest('POST_B2B_DROPDOWNS_UPDATE', workerPostB2BDropdownsUpdate);
}
export default WatchDropdownsSaga;
//--- End: Saga main Action Watcher generator function

function* workerPostB2BDropdownsList(action) {
  const timeOutApiCall = setTimeout(() => { action.callback(timeOutRes) }, 50000);
  try {
    // API calls here
    let reqObj = { apiUrl: apis.postB2BDropdownsListAPI, body: action.body };
    const resObj = yield call(postApiCall, reqObj);
    clearTimeout(timeOutApiCall);
    action.callback(resObj);
  } catch (error) {
    clearTimeout(timeOutApiCall);
    action.callback(errEmptyRes);
  }
}
