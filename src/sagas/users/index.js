import { call, put, takeLatest } from 'redux-saga/effects';
import {postApiCall, getApiCall, putApiCall, deleteCall} from '../../server/ApiCallManager';
import apis from '../../../config/apis.json';

const errEmptyRes = {};
const timeOutRes = {status: '777', message: 'Server not responding'};

//--- Begin: Saga main Action Watcher generator function
function* WatchUserSaga() {
  yield takeLatest('POST_B2B_USERS_LIST', workerPostB2BUsersList);
  yield takeLatest('GET_B2B_USER_VIEW', workerGetB2BUserView);
  yield takeLatest('POST_B2B_USERS_INVITE_CREATE', workerPostB2BUsersInviteCreate);
  yield takeLatest('PUT_B2B_USERS_STATUS_UPDATE', workerPutB2BUsersStatusUpdate);
  yield takeLatest('DELETE_B2B_USERS_DELETE', workerDeleteB2BUsersDelete);
  yield takeLatest('PUT_ADMIN_USER_PASSWORD_UPDATE', workerPutB2BUsersPasswordUpdate);
  yield takeLatest('POST_B2B_USERS_LREPORT_IST', workerPostB2BUsersReportList);
  yield takeLatest('POST_B2B_TOTAL_USERS_LIST', workerPostB2bTotalUsersListAPI); 
  yield takeLatest('GET_B2B_REGISTER_USERS_DATA', workerGetB2BRegisterUsersDataAPI); 
  yield takeLatest('PUT_B2B_REGISTER_USERS_DATA_UPDATE', workerPutB2BRegisterUsersDataUpdateAPI); 
}

export default WatchUserSaga;
//--- End: Saga main Action Watcher generator function

function* workerPostB2BUsersList(action) {
  const timeOutApiCall = setTimeout(() => { action.callback(timeOutRes) }, 50000);
  try {
    // API calls here
    let reqObj = { apiUrl: apis.postB2BUsersListAPI, body: action.body };
    const resObj = yield call(postApiCall, reqObj);
    clearTimeout(timeOutApiCall);
    action.callback(resObj);
  } catch (error) {
    clearTimeout(timeOutApiCall);
    action.callback(errEmptyRes);
  }
}

